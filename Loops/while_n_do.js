// while loop 
let index = 0;

while (index <= 10){
    console.log(`Value of index is ${index}.`);
    index = index + 2
}

// Array:

let myArray = ['flash', 'Spiderman', 'superman']

let arr = 0
while (arr < myArray.length){
    console.log(`Value is ${myArray[arr]}.`);
    arr = arr + 1
}


// do while Loop: 
 // do while loop, runs the result first and run the loop.  

let score = 1
do {
    console.log(`The score is : ${score}. `)
    score = score + 1
 
} while (score <= 5);
